const pick = <T,>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)]

// 無料側ロックのクリフハンガー3行（右手・金運線・人生の転機）
export function buildCliffhangers(): { head: string; tail: string }[] {
  return [
    { head: 'あなたの右手には', tail: pick(CLIFF_RIGHT) },
    { head: 'あなたの金運線が示しているのは', tail: pick(CLIFF_MONEY) },
    { head: 'あなたの人生の転機は', tail: pick(CLIFF_TURNING) },
  ]
}
