const pick = <T,>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)]

// スコア帯に合うラベルを1つ選ぶ
export function pickScoreLabel(score: number): string {
  const s = Number(score) || 70
  const bucket =
    s < 60 ? SCORE_LABELS.toS59 :
    s < 70 ? SCORE_LABELS.to69 :
    s < 80 ? SCORE_LABELS.to79 :
    s < 90 ? SCORE_LABELS.to89 :
             SCORE_LABELS.to100
  return pick(bucket)
}
export const pickPossibility = (): string => pick(POSSIBILITIES)
export const pickCliffhanger = (): string => pick(CLIFFHANGERS)
